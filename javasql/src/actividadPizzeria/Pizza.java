package actividadPizzeria;

public class Pizza {
	private String nombre;
	private String apellido;
	private int id;
	private double sueldo;
	private int telefono;
	private String correo;

	public Pizza(String nombre, String apellido, int id, double sueldo, int telefono, String correo) {
		this.nombre = nombre;
		this.apellido = apellido;
		this.id = id;
		this.sueldo = sueldo;
		this.telefono = telefono;
		this.correo = correo;
	}

}
