package agenda;

public class Persona {
	private int id = 0;
	private String nombre;
	private String apellido1;
	private String apellido2;
	private String correo;

	public Persona(int id, String nombre, String apellido1, String apellido2, String correo) {
		this.id = id;
		this.nombre = nombre;
		this.apellido1 = apellido1;
		this.apellido2 = apellido2;
		this.correo = correo;
	}

}
